public class Ex3 {
    public static void main(String[] args) {
        // 다음 계산 결과를 출력하는 프로그램을 작성하되, 덧셈 연산의 횟수를 최소화하여 작성해 보시오.
        int result1 = 3 + 6; // int 변수 result1을 선언하고 3+6결과를 저장한다.
        System.out.println(result1); // result1 출력
        int result2 = result1 + 9; //int 변수 result2을 선언하고 3+6+9 결과를 저장한다.
        System.out.println(result2); // result2 출력
        int result3 = result2 + 12; //int 변수 result3을 선언하고 3+6+9+12 결과를 저장한다.
        System.out.println(result3); // result3 출력
    }
}
