public class Ex1 {
    public static void main(String[] args) {
        System.out.println();
        int num1 = 10;      // int형 변수 num1선언 후 10을 저장한다
        int num2 = 20;      // num2에 20을 저장한다
        int num3 = 30;      // num3에 30을 저장한다
        int result = (num1 + num2) * num3; // int형 변수 result를 선언하고 (num1+num2)*num3의 결과를 저장하라
        System.out.println(result);     // return을 출력하라
    }
}
// java는 무조건 코드가 메쏘드 안에 들어감
