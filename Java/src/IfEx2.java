public class IfEx2 {
    public static void main(String[] args) {
        int num = 120; // int형 변수 num에 120 값을 저장한다

        if (num % 2 == 0) { // num이 양수이거나 짝수인 조건문을 작성한다
            System.out.println("2의 배수입니다"); // 2의 배수입니다를 출력
        } else {
            System.out.println("2의 배수가 아닙니다"); // 2의 배수가 아닙니다를 출력
        }
    }
}
